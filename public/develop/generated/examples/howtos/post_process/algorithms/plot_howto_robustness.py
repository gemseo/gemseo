"""# Robustness of an optimum

## Problem

After an optimisation, you want to quantify how sensitive the optimal objective
and constraint values are to small perturbations of the design variables.

## Solution

Use [Robustness][gemseo.post.robustness.Robustness], which propagates analytically
a normal distribution with 1% standard deviation on all design variables
(assuming no cross-correlations) using quadratic approximations of the output functions.
The resulting distribution is visualised as a box plot,
showing the spread of function values relative to the optimum.

## Step-by-step guide
"""

from __future__ import annotations

from gemseo import execute_post
from gemseo.settings.post import Robustness_Settings

# %%
# ### 1. Prerequisites
#
# This how-to requires an HDF5 file generated by a previously executed scenario,
# containing gradient evaluations.
# See [How to post-process an HDF5 file][post-process-an-hdf5-file]
# for more information on how to generate and load such a file.
#
# !!! note
#     The `execute_post` function also handles previously executed instances of `MDOScenario`
#     or `OptimzationProblem`.
#
# ### 2. Run the robustness post-processing
#
# The box plot shows, for each function, the extreme values (top and bottom bars),
# the interval containing 95% of the samples (blue boxes),
# and the average (red bar), all expressed relatively to the optimum value:
execute_post(
    "sobieski_mdf_scenario.h5",
    settings_model=Robustness_Settings(save=False, show=True),
)

# %%
# ## Summary
#
# [Robustness][gemseo.post.robustness.Robustness] quantifies the sensitivity of
# the optimum to small input perturbations by propagating a 1% normal distribution
# through quadratic approximations of the output functions.
# Pass any HDF5 file from a previously executed scenario to
# [execute_post()][gemseo.execute_post].
