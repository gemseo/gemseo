r"""# Quadratic approximation of a function

## Problem

After an optimisation, you want to understand the second-order sensitivity
of a function with respect to the design variables,
and identify which variables have the most influence at the optimum.

## Solution

Use [QuadApprox][gemseo.post.quad_approx.QuadApprox], which produces two plots:

- the approximated Hessian matrix $\frac{\partial^2 f}{\partial x_i \partial x_j}$
  estimated by the SR1 method, showing cross-influences between design variables,
- the quadratic approximation of the function around the optimum $x^*$,
  $a_i(t) = 0.5(t - x^*_i)^2 \frac{\partial^2 f}{\partial x_i^2} + (t - x^*_i)
  \frac{\partial f}{\partial x_i} + f(x^*)$,
  highlighting the sensitivity of the function to each variable individually.

## Step-by-step guide
"""

from __future__ import annotations

from gemseo import execute_post
from gemseo.settings.post import QuadApprox_Settings

# %%
# ### 1. Prerequisites
#
# This how-to requires an HDF5 file generated by a previously executed scenario,
# containing gradient evaluations.
# See [How to post-process an HDF5 file][post-process-an-hdf5-file]
# for more information on how to generate and load such a file.
#
# !!! note
#     The `execute_post` function also handles previously executed instances of `MDOScenario`
#     or `OptimzationProblem`.
#
# ### 2. Run the quadratic approximation post-processing
#
# Pass the name of the function to approximate via the `function` setting:
execute_post(
    "sobieski_mdf_scenario.h5",
    settings_model=QuadApprox_Settings(function="-y_4", save=False, show=True),
)

# %%
# ## Summary
#
# [QuadApprox][gemseo.post.quad_approx.QuadApprox] provides two complementary views
# of the second-order sensitivity of a function at the optimum:
# the SR1 Hessian matrix for cross-variable influences,
# and the univariate quadratic approximation for individual variable sensitivity.
# Pass any HDF5 file from a previously executed scenario to
# [execute_post()][gemseo.execute_post].
