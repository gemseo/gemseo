r"""# Influence of variables on the objective

## Problem

After an optimisation, you want to quantify how much each design variable
contributed to the change in the objective function,
from the initial design to the optimal one.

## Solution

Use [VariableInfluence][gemseo.post.variable_influence.VariableInfluence],
which computes the first-order influence of each variable $x_i$ as
$\frac{\mathrm d f}{\mathrm d x_i} \cdot \left(x_{i_*} - x_{i,0}\right)$,
where $x_{i,0}$ is the initial value and $x_{i_*}$ is the optimal value.

## Step-by-step guide
"""

from __future__ import annotations

from gemseo import execute_post
from gemseo.settings.post import VariableInfluence_Settings

# %%
# ### 1. Prerequisites
#
# This how-to requires an HDF5 file generated by a previously executed scenario.
# See [How to post-process an HDF5 file][post-process-an-hdf5-file]
# for more information on how to generate and load such a file.
# !!!note
#     The `execute_post` function also handles previously executed instances of `MDOScenario`
#     or `OptimzationProblem`.

# %%
# ### 2. Run the variable influence analysis
#
execute_post(
    "sobieski_mdf_scenario.h5",
    settings_model=VariableInfluence_Settings(save=False, show=True),
)

# %%
# ## Summary
#
# [VariableInfluence][gemseo.post.variable_influence.VariableInfluence] quantifies
# the first-order contribution of each design variable to the change in objective,
# from the initial to the optimal design.
# Pass any HDF5 file from a previously executed scenario to [execute_post()][gemseo.execute_post].
