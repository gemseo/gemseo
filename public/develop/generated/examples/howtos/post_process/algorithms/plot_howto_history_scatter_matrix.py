"""# Scatter plot matrix of design variables and functions

## Problem

After an optimisation or a DOE, you want a pairwise overview of the bivariate relationships
between selected design variables and output functions.

## Solution

Use [ScatterPlotMatrix][gemseo.post.scatter_plot_matrix.ScatterPlotMatrix],
which produces a matrix of scatter plots for selected variables.
Off-diagonal blocks show pairwise sample distributions;
diagonal blocks approximate each variable's probability distribution
using a kernel-density estimator.

## Step-by-step guide
"""

from __future__ import annotations

from gemseo import execute_post
from gemseo.settings.post import ScatterPlotMatrix_Settings

# %%
# ### 1. Prerequisites
#
# This how-to requires an HDF5 file generated by a previously executed scenario.
# See [How to post-process an HDF5 file][post-process-an-hdf5-file]
# for more information on how to generate and load such a file.
# !!!note
#     The `execute_post` function also handles previously executed instances of `MDOScenario`
#     or `OptimzationProblem`.

# %%
# ### 2. Run the scatter plot matrix post-processing
#
# Use `variable_names` to select the design variables and functions to include:
execute_post(
    "sobieski_mdf_scenario.h5",
    settings_model=ScatterPlotMatrix_Settings(
        variable_names=["x_shared", "x_1", "x_2", "x_3", "-y_4"],
        save=False,
        show=True,
    ),
)

# %%
# ## Summary
#
# [ScatterPlotMatrix][gemseo.post.scatter_plot_matrix.ScatterPlotMatrix] provides
# a pairwise view of selected variables from the optimisation or DOE history.
# Use `variable_names` to restrict the matrix to the variables of interest.
# Pass any HDF5 file from a previously executed scenario to
# [execute_post()][gemseo.execute_post].
