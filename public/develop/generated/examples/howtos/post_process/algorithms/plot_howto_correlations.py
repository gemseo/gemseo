r"""# Correlation analysis of optimisation variables

## Problem

After an optimisation or a DOE, you want to identify which pairs of design
variables, objective and constraints are linearly correlated.

## Solution

Use [Correlations][gemseo.post.correlations.Correlations], which computes
the normalised correlation coefficient

$$R_{xy} = \frac{\sum_{i=1}^n (x_i - \bar{x})(y_i - \bar{y})}
{\sqrt{\sum_{i=1}^n (x_i - \bar{x})^2 \sum_{i=1}^n (y_i - \bar{y})^2}}$$

where $\bar{x}$ and $\bar{y}$ are the sample means and $n$ is the number of samples.

It produces scatter plots for all pairs whose coefficient exceeds a given threshold
(default: 0.95).

## Step-by-step guide
"""

from __future__ import annotations

from gemseo import execute_post
from gemseo.settings.post import Correlations_Settings

# %%
# ### 1. Prerequisites
#
# This how-to requires an HDF5 file generated by a previously executed scenario.
# See [How to post-process an HDF5 file][post-process-an-hdf5-file]
# for more information on how to generate and load such a file.
#     The `execute_post` function also handles previously executed instances of `MDOScenario`
#     or `OptimzationProblem`.

# %%
# ### 2. Run the correlation analysis
#
# Use `func_names` to restrict the analysis to specific functions
# and `coeff_limit` to set the correlation threshold:
execute_post(
    "sobieski_mdf_scenario.h5",
    settings_model=Correlations_Settings(
        func_names=["g_3"],
        coeff_limit=0.95,
        save=False,
        show=True,
    ),
)

# %%
# ## Summary
#
# [Correlations][gemseo.post.correlations.Correlations] produces scatter plots
# for all pairs of variables whose linear correlation coefficient exceeds `coeff_limit`.
# Use `func_names` to focus on specific functions.
# Pass any HDF5 file from a previously executed scenario to
# [execute_post()][gemseo.execute_post].
